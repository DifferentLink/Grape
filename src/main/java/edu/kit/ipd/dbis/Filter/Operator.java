package edu.kit.ipd.dbis.Filter;

/**
 * the enum Operator is used to connect addition, multiplication, substraction and division logically
 */
public enum Operator {
    /**
     * used for addition
     */
    ADD,
    /**
     * used for substraction
     */
    SUB,
    /**
     * used for multiplication
     */
    MULT,
    /**
     * used for division
     */
    DIV
}
