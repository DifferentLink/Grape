package edu.kit.ipd.dbis.Filter;

/**
 * the enum Relation is used to connect addition, multiplication, substraction and division logically
 */
public enum Relation {
    /**
     * codes the relation '='
     */
    EQUAL,
    /**
     * codes the relation '>'
     */
    GREATHERTHAN,
    /**
     * codes the relation '<'
     */
    LESSTHAN,
    /**
     * codes the relation '>='
     */
    GREATHEROREQUAL,
    /**
     * codes the relation '<='
     */
    LESSOREQUAL
}
